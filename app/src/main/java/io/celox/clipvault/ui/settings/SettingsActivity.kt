class SettingsActivity : FragmentActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()

        setContent {
            ClipVaultTheme {
                val app = application as ClipVaultApp
                val ksm = app.keyStoreManager
                var appLockEnabled by remember { mutableStateOf(ksm.isAppLockEnabled()) }
                var biometricEnabled by remember { mutableStateOf(ksm.isAppLockBiometricEnabled()) }
                var passwordGenerated by remember { mutableStateOf(ksm.isAppLockPasswordGenerated()) }
                var showAppLockDialog by remember { mutableStateOf(false) }
                var showDisableAppLockDialog by remember { mutableStateOf(false) }
                var showChangePasswordDialog by remember { mutableStateOf(false) }

                val versionName = try {
                    packageManager.getPackageInfo(packageName, 0).versionName ?: "?"
                } catch (_: Exception) { "?" }

                SettingsScreen(
                    appLockEnabled = appLockEnabled,
                    biometricEnabled = biometricEnabled,
                    passwordGenerated = passwordGenerated,
                    isLicenseActivated = app.licenseManager.isActivated(),
                    licenseEmail = app.licenseManager.getActivatedEmail(),
                    versionName = versionName,
                    onBack = { finish() },
                    onToggleAppLock = { enabled ->
                        if (enabled) {
                            showAppLockDialog = true
                        } else {
                            showDisableAppLockDialog = true
                        }
                    },
                    onChangePassword = { showChangePasswordDialog = true },
                    onToggleBiometric = { enabled ->
                        ksm.setAppLockBiometricEnabled(enabled)
                        biometricEnabled = enabled
                    },
                    onOpenLicense = {
                        startActivity(Intent(this, LicenseActivity::class.java))
                    },
                    onOpenAbout = {
                        startActivity(Intent(this, AboutActivity::class.java))
                    }
                )

                if (showAppLockDialog) {
                    AppLockSetupDialog(
                        onDismiss = { showAppLockDialog = false },
                        onConfirmGenerated = {
                            showAppLockDialog = false
                            val generatedPw = generateSecurePassword()
                            ksm.storeAppLockPassword(generatedPw)
                            ksm.setAppLockPasswordGenerated(true)
                            ksm.setAppLockBiometricEnabled(true)
                            ksm.setAppLockEnabled(true)
                            appLockEnabled = true
                            passwordGenerated = true
                            biometricEnabled = true
                            // UI feedback via toggle state change
                        },
                        onConfirmManual = { password ->
                            showAppLockDialog = false
                            ksm.storeAppLockPassword(password)
                            ksm.setAppLockPasswordGenerated(false)
                            ksm.setAppLockEnabled(true)
                            appLockEnabled = true
                            passwordGenerated = false
                            // UI feedback via toggle state change
                        }
                    )
                }

                if (showDisableAppLockDialog) {
                    AlertDialog(
                        onDismissRequest = { showDisableAppLockDialog = false },
                        title = { Text("App-Sperre deaktivieren?") },
                        text = { Text("Die App ist danach ohne Authentifizierung zugänglich. Die Datenbank bleibt verschlüsselt.") },
                        confirmButton = {
                            TextButton(onClick = {
                                showDisableAppLockDialog = false
                                ksm.clearAppLock()
                                appLockEnabled = false
                                passwordGenerated = false
                                biometricEnabled = ksm.isAppLockBiometricEnabled()
                                // UI feedback via toggle state change
                            }) {
                                Text("Deaktivieren", color = MaterialTheme.colorScheme.error)
                            }
                        },
                        dismissButton = {
                            TextButton(onClick = { showDisableAppLockDialog = false }) {
                                Text("Abbrechen")
                            }
                        }
                    )
                }

                if (showChangePasswordDialog) {
                    ChangePasswordDialog(
                        onDismiss = { showChangePasswordDialog = false },
                        onConfirm = { oldPw, newPw ->
                            showChangePasswordDialog = false
                            val stored = ksm.retrieveAppLockPassword()
                            if (stored == oldPw) {
                                ksm.storeAppLockPassword(newPw)
                                // Dialog closes as confirmation
                            } else {
                                Toast.makeText(this, "Falsches Passwort", Toast.LENGTH_SHORT).show()
                            }
                        }
                    )
                }
            }
        }
    }

    private fun generateSecurePassword(): String {
        val chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%&*"
        val random = SecureRandom()
        return (1..32).map { chars[random.nextInt(chars.length)] }.joinToString("")
    }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SettingsScreen(
    appLockEnabled: Boolean,
    biometricEnabled: Boolean,
    passwordGenerated: Boolean,
    isLicenseActivated: Boolean,
    licenseEmail: String?,
    versionName: String,
    onBack: () -> Unit,
    onToggleAppLock: (Boolean) -> Unit,
    onChangePassword: () -> Unit,
    onToggleBiometric: (Boolean) -> Unit,
    onOpenLicense: () -> Unit,
    onOpenAbout: () -> Unit
) {
    Scaffold(
        modifier = Modifier.windowInsetsPadding(WindowInsets.safeDrawing),
        topBar = {
            TopAppBar(
                title = { Text("Einstellungen", fontWeight = FontWeight.Bold) },
                navigationIcon = {
                    IconButton(onClick = onBack) {
                        Icon(Icons.AutoMirrored.Filled.ArrowBack, contentDescription = "Zurück")
                    }
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.surface
                )
            )
        }
    ) { innerPadding ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(innerPadding)
                .verticalScroll(rememberScrollState())
        ) {
            // --- Security Section ---
            SectionHeader("Sicherheit")

            // DB encryption info (always on, not toggleable)
            SettingsInfoItem(
                icon = Icons.Default.Security,
                title = "Datenbank-Verschlüsselung",
                subtitle = "Immer aktiv (AES-256)"
            )

            // App lock toggle
            SettingsToggleItem(
                icon = Icons.Default.Lock,
                title = "App-Sperre",
                subtitle = when {
                    appLockEnabled && passwordGenerated -> "Gesperrt (Fingerprint)"
                    appLockEnabled -> "Gesperrt (Passwort)"
                    else -> "Deaktiviert"
                },
                checked = appLockEnabled,
                onCheckedChange = onToggleAppLock
            )

            if (appLockEnabled) {
                // "Passwort ändern" only for manual passwords
                if (!passwordGenerated) {
                    SettingsClickItem(
                        icon = Icons.Default.Key,
                        title = "Passwort ändern",
                        subtitle = "App-Sperr-Passwort ändern",
                        onClick = onChangePassword
                    )
                }

                // Biometric toggle
                if (passwordGenerated) {
                    SettingsInfoItem(
                        icon = Icons.Default.Fingerprint,
                        title = "Biometrische Sperre",
                        subtitle = "Immer aktiv (automatisches Passwort)"
                    )
                } else {
                    SettingsToggleItem(
                        icon = Icons.Default.Fingerprint,
                        title = "Biometrische Sperre",
                        subtitle = if (biometricEnabled) "Fingerabdruck/Gesicht zum Entsperren" else "Deaktiviert",
                        checked = biometricEnabled,
                        onCheckedChange = onToggleBiometric
                    )
                }
            }

            HorizontalDivider(modifier = Modifier.padding(horizontal = 16.dp, vertical = 8.dp))

            // --- License Section ---
            SectionHeader("Lizenz")

            SettingsClickItem(
                icon = Icons.Default.Shield,
                title = if (isLicenseActivated) "Lizenz aktiv" else "Lizenz aktivieren",
                subtitle = if (isLicenseActivated) licenseEmail ?: "" else "Kostenlose Version (max. 10 Clips)",
                onClick = onOpenLicense
            )

            HorizontalDivider(modifier = Modifier.padding(horizontal = 16.dp, vertical = 8.dp))

            // --- Info Section ---
            SectionHeader("Info")

            SettingsClickItem(
                icon = Icons.Default.Info,
                title = "Über ClipVault",
                subtitle = "Version $versionName",
                onClick = onOpenAbout
            )
        }
    }
}

@Composable
fun SectionHeader(title: String) {
    Text(
        text = title,
        style = MaterialTheme.typography.labelLarge,
        color = MaterialTheme.colorScheme.primary,
        fontWeight = FontWeight.Bold,
        modifier = Modifier.padding(start = 16.dp, top = 16.dp, bottom = 4.dp)
    )
}

@Composable
fun SettingsToggleItem(
    icon: ImageVector,
    title: String,
    subtitle: String,
    checked: Boolean,
    onCheckedChange: (Boolean) -> Unit
) {
    Row(
        modifier = Modifier
            .fillMaxWidth()
            .clickable { onCheckedChange(!checked) }
            .padding(horizontal = 16.dp, vertical = 12.dp),
        verticalAlignment = Alignment.CenterVertically
    ) {
        Icon(icon, contentDescription = null, tint = MaterialTheme.colorScheme.onSurfaceVariant)
        Spacer(modifier = Modifier.width(16.dp))
        Column(modifier = Modifier.weight(1f)) {
            Text(title, style = MaterialTheme.typography.bodyLarge)
            Text(subtitle, style = MaterialTheme.typography.bodySmall, color = MaterialTheme.colorScheme.onSurfaceVariant)
        }
        Switch(checked = checked, onCheckedChange = onCheckedChange)
    }
}

